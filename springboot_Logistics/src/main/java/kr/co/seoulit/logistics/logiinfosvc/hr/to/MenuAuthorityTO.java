package kr.co.seoulit.logistics.logiinfosvc.hr.to;

public class MenuAuthorityTO {
	String menuCode, menuName, authorityGroupCode, menuLevel, authority;

	public String getMenuCode() {
		return menuCode;
	}

	public void setMenuCode(String menuCode) {
		this.menuCode = menuCode;
	}

	public String getMenuName() {
		return menuName;
	}

	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}

	public String getAuthorityGroupCode() {
		return authorityGroupCode;
	}

	public void setAuthorityGroupCode(String authorityGroupCode) {
		this.authorityGroupCode = authorityGroupCode;
	}
	
	public String getMenuLevel() {
		return menuLevel;
	}

	public void setMenuLevel(String menuLevel) {
		this.menuLevel = menuLevel;
	}

	public String getAuthority() {
		return authority;
	}

	public void setAuthority(String authority) {
		this.authority = authority;
	}
	
}
